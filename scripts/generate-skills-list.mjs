#!/usr/bin/env node

import { mkdir, readFile, writeFile } from "node:fs/promises";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const root = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const catalog = JSON.parse(await readFile(resolve(root, "catalog.json"), "utf8"));
const groups = new Map();
const acronyms = new Map([
  ["ai", "AI"], ["api", "API"], ["css", "CSS"], ["gpt", "GPT"],
  ["mcp", "MCP"], ["qa", "QA"], ["sdk", "SDK"], ["sql", "SQL"],
  ["svg", "SVG"], ["ui", "UI"], ["ux", "UX"],
]);
const categoryDetails = new Map([
  ["Backend & Data", {
    icon: "🧱",
    tagline: "Services, persistence, and the parts that have to stay correct.",
  }],
  ["Cloud & Infrastructure", {
    icon: "☁️",
    tagline: "Platforms, deployments, and life beyond localhost.",
  }],
  ["Communication & Productivity", {
    icon: "✍️",
    tagline: "Say more with less, then hand the work over cleanly.",
  }],
  ["Debugging & Quality", {
    icon: "🧭",
    tagline: "Follow the evidence, find the fault, and verify the fix.",
  }],
  ["Engineering Workflow", {
    icon: "🛠️",
    tagline: "Understand the codebase and keep useful work moving.",
  }],
  ["Frontend & Design", {
    icon: "✦",
    tagline: "Interfaces with taste, motion, accessibility, and sharp edges.",
  }],
  ["Native & Mobile", {
    icon: "📱",
    tagline: "Small screens, native conventions, and thumb-friendly decisions.",
  }],
  ["Planning & Learning", {
    icon: "🧠",
    tagline: "Think clearly before the expensive part begins.",
  }],
  ["Security", {
    icon: "🛡️",
    tagline: "Catch secrets, harden boundaries, and reduce avoidable risk.",
  }],
]);
const anchorFor = (value) => value
  .toLowerCase()
  .replaceAll("&", "and")
  .replace(/[^a-z0-9]+/g, "-")
  .replace(/^-|-$/g, "");
const escapeHtml = (value) => String(value)
  .replaceAll("&", "&amp;")
  .replaceAll("<", "&lt;")
  .replaceAll(">", "&gt;")
  .replaceAll('"', "&quot;");
const titleFor = (skill) => skill.name
  .split("-")
  .map((word) => acronyms.get(word) || word.charAt(0).toUpperCase() + word.slice(1))
  .join(" ");
const truncate = (value, maximum = 110) => {
  if (value.length <= maximum) return value;
  const clipped = value.slice(0, maximum - 1);
  const lastSpace = clipped.lastIndexOf(" ");
  const clean = (lastSpace > maximum * 0.7 ? clipped.slice(0, lastSpace) : clipped)
    .replace(/[,:;.\s—–-]+$/, "");
  return `${clean}…`;
};

const renderCatalogItem = (skill) => [
  `<p><a href="catalog/${escapeHtml(skill.name)}.md"><strong>${escapeHtml(titleFor(skill))}</strong></a> — ${escapeHtml(truncate(skill.summary))}</p>`,
];

const renderSkillPage = (skill) => {
  const categoryAnchor = anchorFor(skill.category);
  const page = [
    "<!-- Generated by scripts/generate-skills-list.mjs. -->",
    "",
    `[← ${skill.category}](../SKILLS.md#${categoryAnchor})`,
    "",
    `# ${titleFor(skill)}`,
    "",
    skill.summary,
    "",
  ];

  if (skill.provenance === "reference-only") {
    page.push(
      "## Original skill",
      "",
      `[View the skill from ${skill.author}](${skill.source})`,
      "",
      `**License:** ${skill.license}`,
      "",
    );
  } else {
    page.push(
      "## Install",
      "",
      "```sh",
      `npx github:Elvis020/hey-skills add ${skill.name} --to ./skills`,
      "```",
      "",
      "## Details",
      "",
      `- **Files:** [View SKILL.md](../${skill.path}/SKILL.md)`,
      `- **License:** ${skill.license}`,
    );
    if (skill.source) {
      page.push(`- **Original source:** [${skill.author}](${skill.source})`);
    }
    page.push("");
    if (skill.provenance === "local") {
      page.push(
        "> Local collection · original source metadata still needs confirmation.",
        "",
      );
    }
  }

  page.push("[Browse all skills](../SKILLS.md)", "");
  return page.join("\n");
};

for (const skill of catalog.skills) {
  if (!groups.has(skill.category)) groups.set(skill.category, []);
  groups.get(skill.category).push(skill);
}

const lines = [
  "# Skills",
  "",
  "A working shelf of playbooks for agents that need to build, debug, design, and ship.",
  "",
  `> **${catalog.skills.length} skills. ${groups.size} shelves. One command to take the lot.**`,
  "",
  "```sh",
  "npx github:Elvis020/hey-skills add --all --to ./skills",
  "```",
  "",
];

if (!catalog.skills.length) {
  lines.push("No skills have been published yet.", "");
} else {
  lines.push(
    "## Pick a shelf",
    "",
    '<table width="100%">',
    "<thead>",
    "<tr><th align=\"left\">Shelf</th><th align=\"left\">What lives here</th><th align=\"right\">Skills</th></tr>",
    "</thead>",
    "<tbody>",
    ...[...groups.keys()].sort().map((category) => {
      const details = categoryDetails.get(category) || { icon: "•", tagline: "Useful agent skills." };
      return `<tr><td>${details.icon} <a href="#${anchorFor(category)}"><strong>${escapeHtml(category)}</strong></a></td><td>${escapeHtml(details.tagline)}</td><td align="right">${groups.get(category).length}</td></tr>`;
    }),
    "</tbody>",
    "</table>",
    "",
    "Pick a shelf, then open a skill to reveal its details and copy the install command.",
    "",
  );

  for (const category of [...groups.keys()].sort()) {
    const details = categoryDetails.get(category) || { icon: "•", tagline: "Useful agent skills." };
    lines.push(
      `<a id="${anchorFor(category)}"></a>`,
      "",
      `## ${details.icon} ${category}`,
      "",
      `*${details.tagline}*`,
      "",
    );
    const skills = groups.get(category).sort((a, b) => a.name.localeCompare(b.name));

    for (const skill of skills) {
      lines.push(...renderCatalogItem(skill));
    }
    lines.push("");
  }
}

lines.push(
  "---",
  "",
  "Built as a personal shelf, shared so useful instructions do not stay trapped on one machine.",
  "",
  "See [ATTRIBUTION.md](ATTRIBUTION.md) for creator and license details.",
  "",
);
const catalogDirectory = resolve(root, "catalog");
await mkdir(catalogDirectory, { recursive: true });
await Promise.all(catalog.skills.map((skill) =>
  writeFile(resolve(catalogDirectory, `${skill.name}.md`), renderSkillPage(skill)),
));
await writeFile(resolve(root, "SKILLS.md"), lines.join("\n"));
console.log(`Generated SKILLS.md and ${catalog.skills.length} skill page(s)`);
