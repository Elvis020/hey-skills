#!/usr/bin/env node

import { existsSync, readFileSync, writeFileSync } from "node:fs";
import { basename, extname, join } from "node:path";
import { execFileSync } from "node:child_process";
import { fileURLToPath } from "node:url";

function readJson(path) {
  return JSON.parse(readFileSync(path, "utf8"));
}

function writeJson(path, value) {
  writeFileSync(path, `${JSON.stringify(value, null, 2)}\n`, "utf8");
}

function fileExists(path) {
  return existsSync(path);
}

function toPosix(path) {
  return path.replace(/\\/g, "/");
}

function dirOf(path) {
  const normalized = toPosix(path);
  const idx = normalized.lastIndexOf("/");
  return idx === -1 ? "" : normalized.slice(0, idx);
}

function topDir(path) {
  const normalized = toPosix(path);
  const first = normalized.split("/").filter(Boolean)[0];
  return first ?? "";
}

function titleCase(text) {
  return text
    .split(/[-_\s]+/)
    .filter(Boolean)
    .map((part) => part.charAt(0).toUpperCase() + part.slice(1))
    .join(" ");
}

function kebab(text) {
  return text
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");
}

function readFirstExisting(projectRoot, candidates) {
  for (const rel of candidates) {
    const abs = join(projectRoot, rel);
    if (fileExists(abs)) return readFileSync(abs, "utf8");
  }
  return null;
}

function extractReadmeDescription(text) {
  if (!text) return null;
  const lines = text.split(/\r?\n/).map((line) => line.trim());
  const chunks = [];
  let current = [];
  for (const line of lines) {
    if (!line) {
      if (current.length > 0) chunks.push(current.join(" "));
      current = [];
      continue;
    }
    if (line.startsWith("#")) continue;
    if (line.startsWith("```")) continue;
    if (line.startsWith("|")) continue;
    if (line.startsWith("<")) continue;
    current.push(line);
    if (current.join(" ").length >= 180) break;
  }
  if (current.length > 0) chunks.push(current.join(" "));
  const picked = chunks.find((chunk) => chunk.length >= 30);
  return picked ?? null;
}

function extractArchitectureProjectType(text) {
  if (!text) return null;
  const lines = text.split(/\r?\n/);
  const idx = lines.findIndex((line) => line.trim() === "## Project Type");
  if (idx === -1) return null;
  const next = lines.slice(idx + 1).map((line) => line.trim()).find(Boolean);
  return next || null;
}

function inferProjectName(projectRoot) {
  const packageJsonPath = join(projectRoot, "package.json");
  if (fileExists(packageJsonPath)) {
    try {
      const pkg = readJson(packageJsonPath);
      if (typeof pkg.name === "string" && pkg.name.trim()) return pkg.name.trim();
    } catch {}
  }
  return basename(projectRoot);
}

function inferFrameworks(projectRoot) {
  const frameworks = new Set();
  const packageJsonPath = join(projectRoot, "package.json");
  if (fileExists(packageJsonPath)) {
    try {
      const pkg = readJson(packageJsonPath);
      const deps = {
        ...(pkg.dependencies ?? {}),
        ...(pkg.devDependencies ?? {}),
      };
      const has = (name) => Boolean(deps[name]);
      if (has("react")) frameworks.add("React");
      if (has("next")) frameworks.add("Next.js");
      if (has("vue")) frameworks.add("Vue");
      if (has("svelte")) frameworks.add("Svelte");
      if (has("astro")) frameworks.add("Astro");
      if (has("express")) frameworks.add("Express");
      if (has("fastify")) frameworks.add("Fastify");
      if (has("@nestjs/core")) frameworks.add("NestJS");
      if (has("tailwindcss")) frameworks.add("Tailwind CSS");
      if (has("vite")) frameworks.add("Vite");
    } catch {}
  }

  const pyproject = readFirstExisting(projectRoot, ["pyproject.toml", "requirements.txt"]);
  if (pyproject) {
    const lower = pyproject.toLowerCase();
    if (lower.includes("fastapi")) frameworks.add("FastAPI");
    if (lower.includes("django")) frameworks.add("Django");
    if (lower.includes("flask")) frameworks.add("Flask");
  }

  return [...frameworks];
}

function inferDescription(projectRoot) {
  const architecture = readFirstExisting(projectRoot, [
    ".codebase-indexer/docs/architecture.md",
  ]);
  const fromArchitecture = extractArchitectureProjectType(architecture);
  if (fromArchitecture) return fromArchitecture;

  const readme = readFirstExisting(projectRoot, [
    "README.md",
    "readme.md",
    "README.rst",
  ]);
  const fromReadme = extractReadmeDescription(readme);
  if (fromReadme) return fromReadme;

  return "Lightweight structural codebase graph generated by understand-lite.";
}

function getGitCommitHash(projectRoot) {
  try {
    return execFileSync("git", ["rev-parse", "HEAD"], {
      cwd: projectRoot,
      encoding: "utf8",
    }).trim();
  } catch {
    return "unknown";
  }
}

function inferNodeType(file) {
  const path = toPosix(file.path);
  const base = basename(path);
  const ext = extname(path).toLowerCase();

  switch (file.fileCategory) {
    case "config":
      return "config";
    case "docs":
    case "markup":
      return "document";
    case "infra":
      if (
        base === ".gitlab-ci.yml" ||
        path.startsWith(".github/workflows/") ||
        path.startsWith(".circleci/") ||
        base === "Jenkinsfile" ||
        base === "Makefile"
      ) {
        return "pipeline";
      }
      if (ext === ".tf" || ext === ".tfvars" || /\.k8s\.(ya?ml)$/i.test(base)) {
        return "resource";
      }
      return "service";
    case "data":
      if (ext === ".sql" || ext === ".csv" || ext === ".tsv") return "table";
      if (ext === ".graphql" || ext === ".gql") return "endpoint";
      return "schema";
    case "script":
    case "code":
    default:
      return "file";
  }
}

function inferComplexity(sizeLines) {
  if (sizeLines <= 80) return "simple";
  if (sizeLines <= 250) return "moderate";
  return "complex";
}

function inferSummary(file, nodeType) {
  const language = file.language === "unknown" ? "Project" : titleCase(file.language);
  const dir = dirOf(file.path);
  const location = dir ? ` in ${dir}` : "";

  if (nodeType === "config") return `${language} configuration file${location}.`;
  if (nodeType === "document") return `${language} documentation or markup file${location}.`;
  if (nodeType === "pipeline") return `${language} build or automation pipeline file${location}.`;
  if (nodeType === "resource") return `${language} infrastructure resource definition${location}.`;
  if (nodeType === "service") return `${language} service or deployment definition${location}.`;
  if (nodeType === "table") return `${language} data or schema artifact${location}.`;
  if (nodeType === "schema") return `${language} schema or contract definition${location}.`;
  if (nodeType === "endpoint") return `${language} API contract or query definition${location}.`;
  return `${language} source file${location}.`;
}

function buildTags(file, nodeType) {
  const tags = new Set([file.fileCategory, file.language, nodeType]);
  const top = topDir(file.path);
  if (top) tags.add(top);
  return [...tags].filter(Boolean);
}

function nodeIdFor(file) {
  return `${inferNodeType(file)}:${toPosix(file.path)}`;
}

function pickEntryPoints(files) {
  const patterns = [
    /(^|\/)(main|index|app|server)\.(ts|tsx|js|jsx|py|go|rs|java|kt|cs)$/i,
    /(^|\/)src\/main\.(ts|tsx|js|jsx|py|go|rs)$/i,
    /(^|\/)src\/app\.(ts|tsx|js|jsx)$/i,
    /(^|\/)manage\.py$/i,
    /(^|\/)__main__\.py$/i,
  ];
  return files
    .filter((file) => file.fileCategory === "code" || file.fileCategory === "script")
    .filter((file) => patterns.some((pattern) => pattern.test(toPosix(file.path))))
    .slice(0, 8);
}

function buildLayers(files) {
  const groups = new Map();
  const push = (key, name, description, nodeId) => {
    let group = groups.get(key);
    if (!group) {
      group = { id: `layer:${key}`, name, description, nodeIds: [] };
      groups.set(key, group);
    }
    group.nodeIds.push(nodeId);
  };

  for (const file of files) {
    const nodeId = nodeIdFor(file);
    const nodeType = inferNodeType(file);
    if (nodeType === "file") {
      push("code", "Code", "Primary source files and executable logic.", nodeId);
    } else if (nodeType === "config") {
      push("config", "Config", "Configuration and environment-shaping files.", nodeId);
    } else if (nodeType === "document") {
      push("docs", "Docs", "Documentation and markup artifacts.", nodeId);
    } else if (nodeType === "table" || nodeType === "schema" || nodeType === "endpoint") {
      push("data", "Data & Contracts", "Schemas, APIs, and data-shaping artifacts.", nodeId);
    } else {
      push("infra", "Infrastructure", "Pipelines, services, and deployment resources.", nodeId);
    }
  }

  return [...groups.values()].filter((layer) => layer.nodeIds.length > 0);
}

function topCodeDirectories(files, limit = 3) {
  const counts = new Map();
  for (const file of files) {
    if (inferNodeType(file) !== "file") continue;
    const top = topDir(file.path) || "root";
    counts.set(top, (counts.get(top) ?? 0) + 1);
  }
  return [...counts.entries()]
    .sort((a, b) => b[1] - a[1] || a[0].localeCompare(b[0]))
    .slice(0, limit)
    .map(([dir]) => dir);
}

function buildTour(files, layers) {
  const tour = [];
  const architectureDocs = files.filter((file) =>
    /^\.codebase-indexer\/docs\/(architecture|implementation)\.md$/i.test(toPosix(file.path)),
  );
  const entryPoints = pickEntryPoints(files);
  const readme = files.find((file) => /(^|\/)README\.md$/i.test(toPosix(file.path)));

  if (architectureDocs.length > 0) {
    tour.push({
      order: tour.length + 1,
      title: "Start With The Index Docs",
      description: "This repo already has lightweight architecture docs. Read them first, then use the graph for navigation and drill-down.",
      nodeIds: architectureDocs.map(nodeIdFor),
    });
  }

  if (readme) {
    tour.push({
      order: tour.length + 1,
      title: "Start With The README",
      description: "Begin with the checked-in documentation to understand the repo's purpose and setup.",
      nodeIds: [nodeIdFor(readme)],
    });
  }

  if (entryPoints.length > 0) {
    tour.push({
      order: tour.length + 1,
      title: "Locate The Entry Points",
      description: "These files are likely starting points for bootstrapping the application or runtime.",
      nodeIds: entryPoints.map(nodeIdFor),
    });
  }

  const topDirs = topCodeDirectories(files);
  if (topDirs.length > 0) {
    const nodeIds = files
      .filter((file) => topDirs.includes(topDir(file.path)))
      .slice(0, 12)
      .map(nodeIdFor);
    tour.push({
      order: tour.length + 1,
      title: "Traverse The Main Code Areas",
      description: "These top-level directories hold most of the code and are the fastest path to architectural understanding.",
      nodeIds,
    });
  }

  const infraLayer = layers.find((layer) => layer.id === "layer:infra");
  if (infraLayer) {
    tour.push({
      order: tour.length + 1,
      title: "Check Runtime And Delivery Files",
      description: "Review the infrastructure layer to see how the project is built, deployed, or automated.",
      nodeIds: infraLayer.nodeIds.slice(0, 8),
    });
  }

  const hotspots = files
    .filter((file) => inferComplexity(file.sizeLines) === "complex")
    .sort((a, b) => b.sizeLines - a.sizeLines)
    .slice(0, 5);
  if (hotspots.length > 0) {
    tour.push({
      order: tour.length + 1,
      title: "Save The Hotspots For Last",
      description: "These larger files are likely to carry more complexity and are easier to approach after the broad map is clear.",
      nodeIds: hotspots.map(nodeIdFor),
    });
  }

  return tour;
}

export function buildLiteGraph({ projectRoot, scanResult, importArtifacts }) {
  const files = (scanResult.files ?? []).map((file) => ({
    ...file,
    path: toPosix(file.path),
  }));
  const gitCommitHash = getGitCommitHash(projectRoot);
  const projectName = inferProjectName(projectRoot);
  const projectDescription = inferDescription(projectRoot);
  const frameworks = inferFrameworks(projectRoot);
  const languages = [...new Set(files.map((file) => file.language).filter(Boolean))].sort();

  const nodes = files.map((file) => {
    const nodeType = inferNodeType(file);
    return {
      id: `${nodeType}:${file.path}`,
      type: nodeType,
      name: basename(file.path),
      filePath: file.path,
      summary: inferSummary(file, nodeType),
      tags: buildTags(file, nodeType),
      complexity: inferComplexity(file.sizeLines),
    };
  });

  const nodeIdByPath = new Map(nodes.map((node) => [node.filePath, node.id]));
  const edges = [];
  for (const [sourcePath, targets] of Object.entries(importArtifacts.importMap ?? {})) {
    const sourceId = nodeIdByPath.get(toPosix(sourcePath));
    if (!sourceId) continue;
    for (const rawTarget of targets) {
      const targetPath = toPosix(rawTarget);
      const targetId = nodeIdByPath.get(targetPath);
      if (!targetId || targetId === sourceId) continue;
      edges.push({
        source: sourceId,
        target: targetId,
        type: "imports",
        direction: "forward",
        weight: 0.7,
      });
    }
  }

  const dedupedEdges = [];
  const seenEdges = new Set();
  for (const edge of edges) {
    const key = `${edge.type}|${edge.source}|${edge.target}`;
    if (seenEdges.has(key)) continue;
    seenEdges.add(key);
    dedupedEdges.push(edge);
  }

  const layers = buildLayers(files);
  const tour = buildTour(files, layers);
  const analyzedAt = new Date().toISOString();

  return {
    graph: {
      version: "1.0.0",
      kind: "codebase",
      project: {
        name: projectName,
        languages,
        frameworks,
        description: projectDescription,
        analyzedAt,
        gitCommitHash,
      },
      nodes,
      edges: dedupedEdges,
      layers,
      tour,
    },
    meta: {
      lastAnalyzedAt: analyzedAt,
      gitCommitHash,
      version: "understand-lite-v1",
      analyzedFiles: files.length,
    },
  };
}

function main(argv) {
  const [projectRoot, scanPath, importPath, graphOut, metaOut] = argv.slice(2);
  if (!projectRoot || !scanPath || !importPath || !graphOut) {
    process.stderr.write(
      "Usage: node build-lite-graph.mjs <project-root> <scan-result.json> <import-map.json> <graph-out.json> [meta-out.json]\n",
    );
    process.exit(1);
  }

  const scanResult = readJson(scanPath);
  const importArtifacts = readJson(importPath);
  const { graph, meta } = buildLiteGraph({ projectRoot, scanResult, importArtifacts });

  writeJson(graphOut, graph);
  if (metaOut) writeJson(metaOut, meta);
}

